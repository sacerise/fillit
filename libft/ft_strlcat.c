#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t j;

	i = 0;
	while (dst[i] && i < size)
		i++;
	j = 0;
	while (src[j] && i < size - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	if (j > 0)
	{
		dst[i] = '\0';
		return (ft_strlen(src) + i - j);
	}
	return (ft_strlen(src) + i);
}
